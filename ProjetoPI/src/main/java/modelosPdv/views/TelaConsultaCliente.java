    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblComponentes.getSelectedRow();
        if (linhaSelecionada >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();
            int idExcluir = Integer.parseInt(model.getValueAt(linhaSelecionada, 0).toString());

            if (ClienteDAO.excluir(idExcluir)) {
                JOptionPane.showMessageDialog(rootPane, "Registro exclu√≠do com sucesso");

                atualizarTabelaClientes();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir o registro");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        if (txtFiltrarNome.getText().strip().equals("")) {
            atualizarTabelaClientes();
        } else {
            String nomeBuscar = txtFiltrarNome.getText();
            ArrayList<Cliente> list = ClienteDAO.buscarPorNomeCliente(nomeBuscar);

            DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();
            model.setRowCount(0);

            for (Cliente cliente : list) {
                model.addRow(new Object[]{
                    cliente.getIdCliente(),
                    cliente.getNomeCliente(),
                    cliente.getEndereco(),
                    cliente.getCPF(),
                    cliente.getTelefone(),
                    cliente.getEmail(),
                    cliente.getSexo(),
                    cliente.getEstadoCivil(),
                    cliente.getDataNascimento()
                });
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tblComponentes.getSelectedRow();

        if (linhaSelecionada >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) tblComponentes.getModel();

            
            int idCliente = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
            String nomeCliente = modelo.getValueAt(linhaSelecionada, 1).toString();
            String endereco = modelo.getValueAt(linhaSelecionada, 2).toString();
            String cpf = modelo.getValueAt(linhaSelecionada, 3).toString();
            String telefone = modelo.getValueAt(linhaSelecionada, 4).toString();
            String email = modelo.getValueAt(linhaSelecionada, 5).toString();
            String sexo = modelo.getValueAt(linhaSelecionada, 6).toString();
            String estadoCivil = modelo.getValueAt(linhaSelecionada, 7).toString();
            String dataNascimento = modelo.getValueAt(linhaSelecionada, 8).toString();

            Cliente cliente = new Cliente(idCliente, nomeCliente, endereco, cpf, telefone, email, sexo, estadoCivil, dataNascimento);

            try {
                TelaCadastroCliente telaCadastroCliente = new TelaCadastroCliente(cliente);
                telaCadastroCliente.setVisible(true);
            } catch (ParseException ex) {
                Logger.getLogger(TelaConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }

            atualizarTabelaClientes();
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    public void atualizarTabelaClientes() {
        ArrayList<Cliente> clientes = ClienteDAO.listar();

        DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();

        model.setRowCount(0);
        for (Cliente cliente : clientes) {
            model.addRow(new Object[]{
                cliente.getIdCliente(),
                cliente.getNomeCliente(),
                cliente.getEndereco(),
                cliente.getCPF(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getSexo(),
                cliente.getEstadoCivil(),
                cliente.getDataNascimento()
            });
        }
    }

