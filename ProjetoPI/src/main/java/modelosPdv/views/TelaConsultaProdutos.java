    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblComponentes.getSelectedRow();
        if (linhaSelecionada >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();
            int idExcluir = Integer.parseInt(model.getValueAt(linhaSelecionada, 0).toString());

            if (ProdutosDAO.excluir(idExcluir)) {
                JOptionPane.showMessageDialog(rootPane, "Registro excluído com sucesso");

                atualizarTabelaProdutos();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir o registro");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int linhaSelecionada = tblComponentes.getSelectedRow();

        if (linhaSelecionada >= 0) {
            DefaultTableModel modelo = (DefaultTableModel) tblComponentes.getModel();

            int id = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString());
            String nomeProduto = modelo.getValueAt(linhaSelecionada, 1).toString();
            double precoVenda = Double.parseDouble(modelo.getValueAt(linhaSelecionada, 2).toString());
            double precoCompra = Double.parseDouble(modelo.getValueAt(linhaSelecionada, 3).toString());
            String fornecedor = modelo.getValueAt(linhaSelecionada, 4).toString();
            int quant = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 5).toString());

            Produtos produto = new Produtos(id, nomeProduto, precoVenda, precoCompra, fornecedor, quant);

            try {
                TelaCadastroProdutos telaCadastroProduto = new TelaCadastroProdutos(produto);
                telaCadastroProduto.setVisible(true);
            } catch (Exception ex) {
                Logger.getLogger(TelaConsultaProdutos.class.getName()).log(Level.SEVERE, null, ex);
            }

            atualizarTabelaProdutos();
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }

        
    }//GEN-LAST:event_btnAlterarActionPerformed

