    private void btnConfirmarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarConsultaActionPerformed
        String nomeProduto = txtNomeConsultar.getText().trim();

        if (nomeProduto.isEmpty()) {
            atualizarTabelaProdutos();
        } else {
            ArrayList<Produtos> produtos = ProdutosDAO.buscarPorNomeProduto(nomeProduto);

            if (produtos.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Produto não encontrado!");
            } else {
                atualizarTabelaProdutos(produtos);
            }
        }
    }//GEN-LAST:event_btnConfirmarConsultaActionPerformed

    public void atualizarTabelaProdutos(ArrayList<Produtos> produtos) {
        DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();
        model.setRowCount(0);

        for (Produtos produto : produtos) {
            model.addRow(new Object[]{
                produto.getId(),
                produto.getNomeProduto(),
                produto.getPrecoVenda(),
                produto.getPrecoCompra(),
                produto.getFornecedor(),
                produto.getQuant()
            });
        }
    }

    public void atualizarTabelaProdutos() {
        ArrayList<Produtos> produtos = ProdutosDAO.listar();

        DefaultTableModel model = (DefaultTableModel) tblComponentes.getModel();
        model.setRowCount(0);

        for (Produtos produto : produtos) {
            model.addRow(new Object[]{
                produto.getId(),
                produto.getNomeProduto(),
                produto.getPrecoVenda(),
                produto.getPrecoCompra(),
                produto.getFornecedor(),
                produto.getQuant()
            });
        }
    }

